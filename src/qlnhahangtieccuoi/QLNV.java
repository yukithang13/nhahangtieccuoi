    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
    String MaNV =txtMSNV.getText();
    try {
    if(MaNV.length()==0) 
        JOptionPane.showMessageDialog(null,"Chọn 1 nhân  viên để xóa","Thông Báo",1);
    else
    {
        if(JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa nhân viên Mã Nhân Viên  " + MaNV,"Thông Báo",2)==0)
    { 
    nv.DeleteData(MaNV);//goi ham xoa du lieu theo ma loai
    ClearData();//Xoa du lieu trong tableModel
    ShowData();//Do du lieu vao table Model
    //setNull();//Xoa trang Textfield
    }
    }
    } 
    catch (SQLException ex) {
    JOptionPane.showMessageDialog(null,"Xóa thất bại","Thong bao",1);
    }
    }//GEN-LAST:event_btnXoaActionPerformed

