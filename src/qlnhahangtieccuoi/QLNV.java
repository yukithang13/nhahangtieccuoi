    private void tabNVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabNVMouseClicked
        // TODO add your handling code here:
        try{
            //Lay chi so dong dang chon
            int row =this.tabNV.getSelectedRow();
            String ml=(this.tabNV.getModel().getValueAt(row,0)).toString();
            ResultSet rs= nv.ShowNhanVien(ml);//Goi ham lay du lieu theo ma
            if(rs.next())//Neu co du lieu
            {
                this.txtMSNV.setText(rs.getString("MaNhanVien"));
                this.txtHoTen.setText(rs.getString("TenNhanVien"));
                this.txtSDT.setText(rs.getString("SoDienThoai"));
                this.txtDiaChi.setText(rs.getString("DiaChi"));
                //this.cmbChucvu.addItem(rs.getString("ChucVu"));
                this.txtChucVu.setText(rs.getString("ChucVu"));
            }
        }
        catch (SQLException e) {
        }
    }//GEN-LAST:event_tabNVMouseClicked

