    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
    String msnv=txtMSNV.getText();
    String hoten=txtHoTen.getText();
    String sdt=txtSDT.getText();
    String diachi=txtDiaChi.getText();
    String chucvu=txtChucVu.getText();
    if(msnv.length()==0 || hoten.length()==0 ||sdt.length()==0 ||diachi.length()==0||chucvu.length()==0) 
        JOptionPane.showMessageDialog(null,"Vui long nhap day du ","Thong bao",1);
    else
        if( hoten.length()>30) 
            JOptionPane.showMessageDialog(null,"ten duoi la 30 ky tu","Thong bao",1);
        else 
         {
        try {
            if(cothem==true)
            {
                nv.InsertData(msnv, hoten, sdt, diachi, chucvu);
                JOptionPane.showMessageDialog(null,"Cập nhập thành công","Thong bao",1);
            }
            else //Luu cho sua
            {
                nv.EditData(msnv, hoten, sdt, diachi, chucvu); 
            }
            ClearData(); //goi ham xoa du lieu tron tableModel
            ShowData(); //Do lai du lieu vao Table Model
            }
        catch (SQLException ex) 
        {
        JOptionPane.showMessageDialog(null,"Cap nhat that bai","Thong bao",1);
        } 
//        setKhoa(false);
//        setButton(true);
        }
        
        
        
    }//GEN-LAST:event_btnLuuActionPerformed

